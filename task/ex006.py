n = float(input("Digite um número: "))
dobro = n * 2
triplo = n * 3
raiz_quadrada = n ** 0.5
print(f"O dobro de {n} é {dobro}.")
print(f"O triplo de {n} é {triplo}.")
print(f"A raiz quadrada de {n} é {raiz_quadrada:.2f}.")
