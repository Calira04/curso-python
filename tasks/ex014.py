temp = float(input("Digite a temperatura em Celsius: "))

temp_fahrenheit = (temp * 9/5) + 32

print(f"A temperatura em Fahrenheit é: {temp_fahrenheit:.2f} °F")
print("Conversão de temperatura concluída com sucesso!")
